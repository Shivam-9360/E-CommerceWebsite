function darkMode() {
    var aboutUsMainLogo = document.getElementById("aboutUsMainLogo");
    aboutUsMainLogo.src = "../images/darkmode_logo.png";
    aboutUsMainLogo.width = "100%";
}

function lightMode() {
    var aboutUsMainLogo = document.getElementById("aboutUsMainLogo");
    aboutUsMainLogo.src = "../images/darkmode_logo.png";
    aboutUsMainLogo.width = "100%";
}